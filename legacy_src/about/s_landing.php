function sol_chg(direct){
	var nowPage = $("#solDivcnt").val();
	
	if(direct == "prev"){
		nowPage--;
	}else{
		nowPage++;
	}
	
	// 첫페이지 전이라면 마지막 페이지로
	if(nowPage < 1){
		nowPage = 4;
	}

	// 마지막 페이지 다음이라면 첫 페이지로
	if(nowPage > 4){
		nowPage = 1;
	}

	$("#solDivcnt").val(nowPage);
	pageChange(nowPage);

}

