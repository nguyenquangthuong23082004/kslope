<script>
    document.querySelectorAll(
        ".process-item, .process-item-2, .process-item-3, .process-item-4"
    ).forEach(function (item) {

        const leftBox = item.querySelector(".left-box");

        if (!leftBox) return;

        leftBox.addEventListener("mouseenter", function () {
            item.classList.add("active");
        });

        leftBox.addEventListener("mouseleave", function () {
            item.classList.remove("active");
        });

    });
    document.addEventListener("DOMContentLoaded", function () {

        document.querySelectorAll(".law-gallery-wrap").forEach(function (gallery) {

            const mainEl = gallery.querySelector(".law-main-swiper");
            const thumbEl = gallery.querySelector(".law-thumb-swiper");
            const nextEl = gallery.querySelector(".law-next");
            const prevEl = gallery.querySelector(".law-prev");

            if (!mainEl || !thumbEl) return;

            const thumbSwiper = new Swiper(thumbEl, {
                slidesPerView: "auto",
                spaceBetween: 10,
                watchSlidesProgress: true,
                slideToClickedSlide: true,
            });

            new Swiper(mainEl, {
                slidesPerView: 1,
                loop: true,
                navigation: {
                    nextEl: nextEl,
                    prevEl: prevEl,
                },
                thumbs: {
                    swiper: thumbSwiper,
                }
            });

        });

        document.querySelectorAll('.ci-subtab').forEach(function (scrollElement) {

            scrollElement.addEventListener('wheel', function (e) {
                scrollElement.scrollLeft += e.deltaY;
                e.preventDefault();
            });

            let isMouseDown = false;
            let startX;
            let startScroll;
            let isDragging = false;

            scrollElement.addEventListener('mousedown', function (e) {
                if (e.button !== 0) return;

                isMouseDown = true;
                isDragging = false;
                startX = e.clientX;
                startScroll = scrollElement.scrollLeft;
                scrollElement.style.cursor = 'grabbing';
                e.preventDefault();
            });

            scrollElement.addEventListener('mousemove', function (e) {
                if (!isMouseDown) return;

                const deltaX = e.clientX - startX;

                if (Math.abs(deltaX) > 5) {
                    isDragging = true;
                }

                scrollElement.scrollLeft = startScroll - deltaX;
                e.preventDefault();
            });

            document.addEventListener('mouseup', function () {
                if (isMouseDown) {
                    isMouseDown = false;
                    scrollElement.style.cursor = 'grab';

                    setTimeout(function () {
                        isDragging = false;
                    }, 10);
                }
            });

            scrollElement.addEventListener('mouseleave', function () {
                if (isMouseDown) {
                    isMouseDown = false;
                    scrollElement.style.cursor = 'grab';
                }
            });

            scrollElement.addEventListener('click', function (e) {
                if (isDragging) {
                    e.preventDefault();
                    e.stopPropagation();
                    e.stopImmediatePropagation();
                }
            }, true);
        });


        const container = document.querySelector(".ci-subtab");
        const activeTab = document.querySelector(".ci-subtab .is-active");

        if (container && activeTab) {

            const targetLeft =
                activeTab.offsetLeft -
                container.clientWidth / 2 +
                activeTab.clientWidth / 2;

            smoothScrollTo(container, targetLeft, 600);
        }


        function smoothScrollTo(el, targetLeft, duration = 600) {
            const startLeft = el.scrollLeft;
            const diff = targetLeft - startLeft;
            const startTime = performance.now();

            const ease = (t) => t * t * (3 - 2 * t);

            function step(now) {
                const t = Math.min(1, (now - startTime) / duration);
                el.scrollLeft = startLeft + diff * ease(t);
                if (t < 1) requestAnimationFrame(step);
            }

            requestAnimationFrame(step);
        }

    });
</script>

